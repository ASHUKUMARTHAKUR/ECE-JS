function onClick() {
    alert('Button clicked (onclick)');
}

function myMouseDown() {
    alert('Mouse button pressed (onmousedown)');
}

function myMouseUp() {
    alert('Mouse button released (onmouseup)');
}

function myMouseOver() {
    alert('Mouse is over the button (onmouseover)');
}

function myMouseOut() {
    alert('Mouse left the button (onmouseout)');
}

function myMouseMove() {
    alert('Mouse moved over the button (onmousemove)');
}

function myDoubleClick() {
    alert('Button double-clicked (ondblclick)');
}

